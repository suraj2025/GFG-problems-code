class Solution
{
    long minDiff(long a[] ,int N,int K)
    {
        Arrays.sort(a);
        long res=a[K-1]-a[0];
        for(int i=1;i<N-K+1;i++){
            res=Math.min(res,a[K-1+i]-a[i]);
        }
        return res;
    }
}


